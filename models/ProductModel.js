const mongoose = require('mongoose');
var Schema = mongoose.Schema;
const productSchema = new Schema({
  first_name: {
    type: String,
    required: true,
  },
  last_name: {
    type: String,
    required: true,
  },
  phone_number: {
    type: Number,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  country: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  pincode: {
    type: Number,
    required: true,
  },
});

productSchema.set('timestamp', true);

module.exports =
  mongoose.models.Guests || mongoose.model('Guests', productSchema);
